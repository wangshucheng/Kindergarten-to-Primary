/** 在某个韵母串中找出应标调的元音位置（a>o>e>i>u>ü，iu/ui 取末位） */
function toneVowelIndex(final) {
  for (const v of ['a', 'o', 'e']) {
    const i = final.indexOf(v);
    if (i !== -1) return i;
  }
  // 只剩 i/u/ü（含 iu / ui）
  const iu = final.indexOf('iu');
  if (iu !== -1) return iu + 1; // u
  const ui = final.indexOf('ui');
  if (ui !== -1) return ui; // i
  for (const v of ['ü', 'u', 'i']) {
    const i = final.indexOf(v);
    if (i !== -1) return i;
  }
  return -1;
}

function addTone(final, tone) {
  const idx = toneVowelIndex(final);
  if (idx === -1) return final;
  const ch = final[idx];
  const marked = (VOWEL_MARKS[ch] || [ch, ch, ch, ch])[tone - 1];
  return final.slice(0, idx) + marked + final.slice(idx + 1);
}

